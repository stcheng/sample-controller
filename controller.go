func newBar(foo *samplev1alpha1.Foo) *samplev1alpha1.Bar {
	var replicas int32
	replicas = 1
	return &samplev1alpha1.Bar{
		TypeMeta: metav1.TypeMeta{
			Kind:       "Bar",
			APIVersion: "samplecontroller.k8s.io/v1alpha1",
		},
		ObjectMeta: metav1.ObjectMeta{
			Name: foo.Spec.BarName,
		},
		Spec: samplev1alpha1.BarSpec{
			DeploymentName: foo.Spec.DeploymentName,
			Replicas:       &replicas,
		},
	}
}

func newDeploymentBar(bar *samplev1alpha1.Bar) *appsv1.Deployment {
	labels := map[string]string{
		"app":        "nginx",
		"controller": bar.Name,
	}
	return &appsv1.Deployment{
		ObjectMeta: metav1.ObjectMeta{
			Name:      bar.Spec.DeploymentName,
			Namespace: bar.Namespace,
			OwnerReferences: []metav1.OwnerReference{
				*metav1.NewControllerRef(bar, samplev1alpha1.SchemeGroupVersion.WithKind("Bar")),
			},
		},
		Spec: appsv1.DeploymentSpec{
			Replicas: bar.Spec.Replicas,
			Selector: &metav1.LabelSelector{
				MatchLabels: labels,
			},
			Template: corev1.PodTemplateSpec{
				ObjectMeta: metav1.ObjectMeta{
					Labels: labels,
				},
				Spec: corev1.PodSpec{
					Containers: []corev1.Container{
						{
							Name:  "nginx",
							Image: "nginx:latest",
						},
					},
				},
			},
		},
	}
}
